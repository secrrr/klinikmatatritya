    Green Laser adalah alat terapi mata berbasis sinar laser berwarna hijau yang digunakan untuk menangani berbagai
    gangguan retina. Di Klinik Mata Tritya, Green Laser digunakan dalam prosedur panretinal photocoagulation (PRP) dan
    barrage laser, dua tindakan penting untuk mencegah kebutaan akibat penyakit retina kronis.

    PRP Laser membantu menutup pembuluh darah abnormal pada pasien dengan retinopati diabetik atau glaukoma
    neovaskular, sedangkan barrage laser digunakan untuk memperkuat area retina yang mengalami robekan atau
    berisiko lepas. Prosedur ini dilakukan tanpa sayatan dan tidak memerlukan rawat inap, menjadikannya solusi efektif
    dan nyaman bagi pasien.

    <h3>Manfaat Klinis Green Laser di Klinik Mata Tritya</h3>

    <h4>Untuk Pasien</h4>
    <ul>
        <li>Prosedur cepat, aman, dan minim rasa sakit</li>
        <li>Tidak memerlukan pembedahan atau rawat inap</li>
        <li>Membantu mencegah kebutaan akibat komplikasi retina</li>
        <li>Cocok untuk pasien dengan diabetes, hipertensi, atau riwayat gangguan retina</li>
        <li>Pemulihan cepat dan dapat kembali beraktivitas dalam waktu singkat</li>
    </ul>

    <h4>Untuk Dokter & Klinik</h4>
    <ul>
        <li>Memberikan kontrol presisi terhadap area retina yang ditarget</li>
        <li>Mendukung terapi retina jangka panjang dengan hasil yang stabil</li>
        <li>Terintegrasi dalam layanan bedah mikro dan retina Klinik Mata Tritya</li>
        <li>Meningkatkan efektivitas pengobatan dan kenyamanan pasien</li>
    </ul>

    <h3>Kapan Pemeriksaan dan Tindakan Green Laser Direkomendasikan?</h3>

    Green Laser direkomendasikan untuk pasien dengan:
    <ul>
        <li>Retinopati diabetik (komplikasi retina akibat diabetes)</li>
        <li>Robekan retina atau risiko pelepasan retina</li>
        <li>Degenerasi retina atau gangguan pembuluh darah retina</li>
        <li>Glaukoma neovaskular atau kondisi retina yang memerlukan fotokoagulasi</li>
        <li>Pasien dengan kondisi retina yang memerlukan stabilisasi retina</li>
    </ul>

    Di Klinik Mata Tritya, tindakan Green Laser dilakukan setelah evaluasi menyeluruh oleh dokter spesialis retina, dan
    menjadi bagian dari protokol terapi retina yang aman, cepat, dan efektif.
