 @section('content')
     Humphrey Visual Field (HVF) adalah alat diagnostik canggih yang digunakan untuk mengukur luas dan kualitas lapang
     pandang mata seseorang. Pemeriksaan ini sangat penting dalam mendeteksi gangguan pada saraf optik, terutama pada kasus
     glaukoma, stroke, atau kelainan neurologis lainnya.

     Di Klinik Mata Tritya, HVF digunakan sebagai bagian dari pemeriksaan lanjutan untuk pasien dengan risiko glaukoma atau
     keluhan penglihatan yang tidak biasa. Dengan teknologi pemetaan visual otomatis, alat ini memberikan hasil akurat yang
     membantu dokter dalam menentukan diagnosis dan rencana terapi.
     <h3>Manfaat Pemeriksaan dengan HVF di Klinik Mata Tritya</h3>
     Untuk Pasien

     <ul>
         <li>Pemeriksaan tidak invasif dan berlangsung dalam waktu singkat</li>
         <li>Memberikan gambaran menyeluruh tentang kemampuan melihat di area samping (perifer)</li>
         <li>Membantu deteksi dini glaukoma sebelum gejala muncul</li>
         <li>Cocok untuk pasien dengan keluhan penglihatan kabur, nyeri mata, atau riwayat stroke</li>
     </ul>

     <h3>Untuk Dokter & Klinik</h3>

     <ul>
         <li>Memberikan data visual yang presisi untuk analisis fungsi saraf optik</li>
         <li>Mendukung diagnosis glaukoma, neuropati optik, dan gangguan neurologis</li>
         <li>Terintegrasi dalam sistem rekam medis digital Klinik Mata Tritya</li>
         <li>Meningkatkan akurasi terapi dan pemantauan perkembangan penyakit</li>
     </ul>

     <h3>Kapan Pemeriksaan HVF Direkomendasikan?</h3>
     Pemeriksaan dengan Humphrey Visual Field direkomendasikan untuk:

     <ul>
         <li>Pasien dengan risiko atau riwayat glaukoma</li>
         <li>Individu dengan keluhan penglihatan menyempit atau kehilangan penglihatan perifer</li>
         <li>Pasien pasca stroke atau trauma kepala yang memengaruhi penglihatan</li>
         <li>Evaluasi lanjutan bagi pasien dengan kelainan retina atau saraf optik</li>
         <li>Pemeriksaan rutin bagi lansia atau pasien dengan penyakit sistemik seperti diabetes dan hipertensi</li>
     </ul>

     Di Klinik Mata Tritya, HVF digunakan oleh dokter spesialis mata sebagai bagian dari pemeriksaan menyeluruh dan
     pemantauan terapi jangka panjang.
