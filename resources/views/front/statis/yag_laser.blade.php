    YAG Laser (Yttrium-Aluminum-Garnet Laser) adalah teknologi laser medis yang digunakan untuk
    mengatasi berbagai kondisi mata, terutama posterior capsular opacification (PCO) atau katarak
    sekunder yang dapat terjadi setelah operasi katarak. YAG Laser juga digunakan untuk terapi
    glaukoma sudut tertutup.

    Di Klinik Mata Tritya, prosedur YAG Laser dilakukan secara rawat jalan tanpa perlu operasi
    invasif. Tindakan ini cepat, aman, dan minim risiko dengan waktu pemulihan yang sangat singkat.

    <h3>Manfaat YAG Laser di Klinik Mata Tritya</h3>

    <h4>Untuk Pasien</h4>
    <ul>
        <li>Prosedur non-invasif tanpa sayatan atau jahitan</li>
        <li>Waktu tindakan sangat singkat, hanya sekitar 5-10 menit</li>
        <li>Tidak memerlukan rawat inap atau pembiusan umum</li>
        <li>Hasil langsung terasa, penglihatan membaik dalam beberapa jam</li>
        <li>Risiko komplikasi sangat minimal</li>
    </ul>

    <h4>Untuk Dokter & Klinik</h4>
    <ul>
        <li>Presisi tinggi dalam membuka kapsul lensa yang keruh</li>
        <li>Kontrol energi laser yang akurat dan aman</li>
        <li>Dapat dilakukan di ruang rawat jalan dengan efisien</li>
        <li>Standar perawatan modern untuk penanganan PCO dan glaukoma</li>
    </ul>

    <h3>Kapan YAG Laser Direkomendasikan?</h3>

    YAG Laser digunakan untuk kondisi berikut:
    <ul>
        <li>Posterior Capsular Opacification (PCO) atau katarak sekunder pascaoperasi katarak</li>
        <li>Glaukoma sudut tertutup akut atau kronik</li>
        <li>Penglihatan kabur atau berkabut setelah operasi katarak</li>
        <li>Gangguan penglihatan akibat membran kapsul yang menebal</li>
        <li>Kondisi lain yang memerlukan capsulotomy atau iridotomy laser</li>
    </ul>

    Di Klinik Mata Tritya, tindakan YAG Laser dilakukan oleh dokter spesialis mata berpengalaman
    dengan peralatan laser terkini, memastikan hasil optimal dan kenyamanan maksimal bagi pasien.
