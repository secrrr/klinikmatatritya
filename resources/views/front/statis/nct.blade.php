    Non-Contact Tonometry (NCT) adalah alat pemeriksaan mata yang digunakan untuk mengukur tekanan intraokular
    (tekanan bola mata) tanpa menyentuh langsung permukaan mata pasien. Pemeriksaan ini sangat penting dalam
    mendeteksi risiko glaukoma, yaitu penyakit mata yang dapat menyebabkan kebutaan permanen jika tidak ditangani
    sejak dini.

    Di Klinik Mata Tritya, NCT menjadi bagian dari pemeriksaan standar untuk pasien dengan keluhan penglihatan atau
    riwayat keluarga glaukoma. Teknologi ini menggunakan hembusan udara ringan untuk mengukur respons kornea,
    sehingga hasilnya cepat, akurat, dan nyaman bagi pasien.

    <h3>Manfaat Pemeriksaan dengan NCT di Klinik Mata Tritya</h3>

    <h4>Untuk Pasien</h4>
    <ul>
        <li>Tidak ada kontak langsung dengan mata, sehingga minim risiko iritasi</li>
        <li>Pemeriksaan berlangsung cepat dan tidak menimbulkan rasa sakit</li>
        <li>Cocok untuk usia termasuk anak-anak dan lansia</li>
        <li>Memberikan rasa aman bagi pasien yang sensitif terhadap alat pemeriksaan konvensional</li>
    </ul>

    <h4>Untuk Dokter & Klinik</h4>
    <ul>
        <li>Mendusung deteksi dini glaukoma dan gangguan tekanan bola mata</li>
        <li>Memberikan data awal yang akurat sebelum pemeriksaan lanjutan</li>
        <li>Meningkatkan efisiensi layanan dan kenyamanan pasien</li>
        <li>Terintegrasi dalam alur pemeriksaan mata menyeluruh di Klinik Mata Tritya</li>
    </ul>

    <h3>Kapan Pemeriksaan ARK Direkomendasikan?</h3>

    Pemeriksaan dengan Non-Contact Tonometry direkomendasikan untuk:
    <ul>
        <li>Pasien dengan keluhan penglihatan kabur, silau, atau mata lelah</li>
        <li>Pemeriksaan refraksi untuk kebutuhan kacamata atau lensa kontak</li>
        <li>Screening awal sebelum tindakan medis seperti operasi katarak atau LASIK</li>
        <li>Pemeriksaan rutin untuk anak sekolah, pekerja kantoran, dan lansia</li>
    </ul>

    Di Klinik Mata Tritya, pemeriksaan NCT dilakukan oleh tenaga medis terlatih dengan peralatan modern dan standar
    pelayanan yang profesional, memastikan hasil akurat dan pengalaman pemeriksaan yang nyaman bagi setiap pasien.
