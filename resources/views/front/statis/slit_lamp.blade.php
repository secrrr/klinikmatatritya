    Slit Lamp adalah alat pemeriksaan mata berbasis mikroskop yang digunakan oleh dokter spesialis mata untuk melihat
    dan memeriksa struktur mata secara detail. Pemeriksaan ini memungkinkan visualisasi langsung segmen anterior
    mata—termasuk
    kornea, iris, lensa, dan konjungtiva—dengan pencahayaan intens dan pembesaran tinggi.

    Di Klinik Mata Tritya, slit lamp digunakan sebagai bagian dari pemeriksaan standar untuk mendeteksi berbagai
    kelainan mata seperti infeksi, peradangan, luka kornea, katarak, dan glaukoma. Pemeriksaan ini dilakukan langsung
    oleh dokter mata yang kompeten dan berpengalaman.

