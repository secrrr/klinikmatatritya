    Mikroskop bedah mata dan mesin phacoemulsification adalah dua perangkat utama dalam tindakan
    operasi katarak modern. Mikroskop memberikan visualisasi presisi tinggi selama tindakan bedah,
    sementara mesin phacoemulsification berfungsi untuk menghancurkan dan menghisap lensa keruh
    (katarak) menggunakan teknologi ultrasound.

    Di Klinik Mata Tritya, kedua alat ini digunakan dalam prosedur operasi katarak one-day care,
    yang memungkinkan pasien pulang di hari yang sama. Teknologi ini mendukung penyembuhan yang
    cepat, luka komplikai lebih rendah, dan hasil penglihatan yang maksimal.

    <h3>Manfaat Klinik Mikroskop & Phacoemulsification di Klinik Mata Tritya</h3>

    <h4>Untuk Pasien</h4>
    <ul>
        <li>Prosedur minim trauma dengan sayuran kecil</li>
        <li>Waktu operasi lebih singkat, umumnya hanya 15-30 menit konvensional</li>
        <li>Risiko infeksi dan komplikasi lebih rendah</li>
        <li>Hasil visual stabil dan tajam setelah operasi</li>
        <li>Bisa langsung pulang setelah tindakan (tanpa rawat inap)</li>
    </ul>

    <h4>Untuk Dokter & Klinik</h4>
    <ul>
        <li>Visualisasi presisi tinggi selama tindakan bedah</li>
        <li>Kontrol penuh atas teknik bedah mitra dan mitra modern</li>
        <li>Mendukung teknik bedah intra dan mitra tenam modern</li>
        <li>Terintegrasi dalam ruang operasi berstandar tinggi Klinik Mata Tritya</li>
    </ul>

    <h3>Kapan Pemeriksaan dan Tindakan dengan Alat Ini Direkomendasikan?</h3>

    Mikroskop dan phacoemulsification digunakan dalam:
    <ul>
        <li>Operasi katarak dengan teknik kecil modern (phaco)</li>
        <li>Bedah retraktif untuk kreksi kebaluan refraksi tertentu</li>
        <li>Tindakan mikro bedah mata untuk mata yang membutuhkan tinggi</li>
        <li>Nasi visual stabil dan tajam setelah pengangkatan katarak</li>
        <li>Pasien dengan kebutuhan lensa tanam (IOL) yang disesuaikan secara individual</li>
    </ul>

    Di Klinik Mata Tritya, tindakan ini dilakukan oleh dokter spesialis mata berpengalaman dengan
    dukungan teknologi terkini dan fasilitas operasi modern, sehingga Anda dapat menjalani prosedur
    dengan tingkat keamanan dan hasil terbaik.
