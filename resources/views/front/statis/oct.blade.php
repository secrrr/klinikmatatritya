    Optical Coherence Tomography (OCT) adalah alat pencitraan diagnostik non-invasif yang digunakan untuk
    memetakan struktur retina dan saraf mata secara detail. Teknologi ini bekerja dengan memanfaatkan cahaya untuk
    menghasilkan gambar penampang berlapis tingkat tinggi, memungkinkan dokter melihat lapisan retina
    secara mikroskopis tanpa menyentuh mata pasien.

    Di Klinik Mata Tritya, OCT digunakan sebagai bagian dari pemeriksaan lanjutan untuk mendeteksi berbagai gangguan
    mata seperti glaukoma, degenerasi makula, retinopati diabetik, dan edema retina. Pemeriksaan ini sangat penting
    untuk diagnosis dini dan pemantauan perkembangan penyakit mata kronis.

    <h3>Manfaat Klinis OCT Laser di Klinik Mata Tritya</h3>

    <h4>Untuk Pasien</h4>
    <ul>
        <li>Pemeriksaan cepat, aman, dan tanpa rasa sakit</li>
        <li>Tidak memerlukan kontak langsung dengan mata</li>
        <li>Memberikan gambaran detail kondisi retina dan saraf optik</li>
        <li>Membantu diagnosis dini sebelum gejala berat muncul</li>
        <li>Cocok untuk pasien dengan diabetes, glaukoma, atau gangguan retina lainnya</li>
    </ul>

    <h4>Untuk Dokter & Klinik</h4>
    <ul>
        <li>Memberikan visualisasi mikroskopis lapisan retina dan saraf mata</li>
        <li>Mendukung diagnosis dan pemantauan penyakit mata kronis</li>
        <li>Terintegrasi dalam layanan bedah mikro dan retina Klinik Mata Tritya</li>
        <li>Meningkatkan akurasi terapi dan efektivitas pemantauan yang panjang</li>
