    <h3>Kapan Pemeriksaan dan Tindakan OCT Laser Direkomendasikan?</h3>

    OCT direkomendasikan untuk pasien dengan:
    <ul>
        <li>Glaukoma atau tekanan bola mata tinggi</li>
        <li>Retinopati diabetik atau gangguan pembuluh darah retina</li>
        <li>Degenerasi makula terkait usia (AMD)</li>
        <li>Edema retina atau penumpukan tajam penglihatan</li>
        <li>Riwayat penyakit retina atau gangguan saraf optik retina</li>
        <li>Pemeriksaan lanjutan setelah hasil slit lamp atau visual field menunjukkan kelainan</li>
    </ul>

    Di Klinik Mata Tritya, OCT dilakukan oleh dokter spesialis mata sebagai bagian dari protokol pemeriksaan retina dan
    saraf optik yang komprehensif, dengan dukungan teknologi terkini dan interpretasi hasil yang detail dan akurat.
